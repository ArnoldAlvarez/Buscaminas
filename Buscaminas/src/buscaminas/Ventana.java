    @Override
    public void actionPerformed(ActionEvent e){
        Cuadro temp2 =(Cuadro)e.getSource();
        if(temp2.lugarMinado())
        {
           
            temp2.setIcon(new ImageIcon(getClass().getResource("carita.png")));
            javax.swing.JOptionPane.showMessageDialog(this, "Perdiste");
             
            this.contador=0;
        }else{
            temp2.setBackground(Color.ORANGE);
            temp2.setOpaque(true);
            temp2.setBorderPainted(false);
            contador++;
            if((numeroCuadros-contadorMinas)==contador)
                javax.swing.JOptionPane.showMessageDialog(this, "Ganaste");
        }
        
    }

