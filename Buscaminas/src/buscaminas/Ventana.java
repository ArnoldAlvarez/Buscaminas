    //Metodo de inicio del juego
    public void iniciar(){
        this.cuadricula.removeAll();
        int nFil, mCol;
        nFil = Integer.parseInt(this.filas.getText());
        mCol = Integer.parseInt(this.columnas.getText());
        
        this.cuadricula.setLayout(new java.awt.GridLayout(nFil,mCol));
        
        this.numeroCuadros=nFil*mCol;
        this.contadorMinas=0;
        for(int i=0;i<nFil;i++){
            for(int j=0;j<mCol;j++){
                
               Cuadro temp=new Cuadro();
               temp.addActionListener(this);
               
               if(temp.lugarMinado())
                  this.contadorMinas++;
              
               temp.setVisible(true);
               this.cuadricula.add(temp);
            }
        }
    }
    
    
    
    
    
    
